// Seeded random number generator (same as before)
function sfc32(a, b, c, d) {
  return function () {
    a >>>= 0; b >>>= 0; c >>>= 0; d >>>= 0;
    var t = (a + b) | 0;
    a = b ^ b >>> 9;
    b = c + (c << 3) | 0;
    c = (c << 21) | d;
    d = d + 1 | 0;
    t = t + d | 0;
    c = c + t | 0;
    return (t >>> 0) / 4294967296;
  }
}

function getSeededRandom(seedString) {
  let h = 1779033703 ^ seedString.length;
  for (let i = 0; i < seedString.length; i++) {
    h = Math.imul(h ^ seedString.charCodeAt(i), 3432918353);
    h = h << 13 | h >>> 19;
  }

  const seed = function () {
    h = Math.imul(h ^ h >>> 16, 2246822507);
    h = Math.imul(h ^ h >>> 13, 3266489909);
    return (h ^= h >>> 16) >>> 0;
  };

  return sfc32(seed(), seed(), seed(), seed());
}

export const generateReviewsForSlug = (slug) => {
  const rng = getSeededRandom(slug);
  const numReviews = 3;
  const reviews = [];

  // Get the review templates for this slug, or use generic fallback
  const templates = reviewTemplates[slug] || [
    "Great article! Very informative and well-written.",
    "This really helped me understand the topic better. Thanks for sharing!",
    "Excellent guide with practical tips I can actually use."
  ];

  // Create a copy of arrays to pick from
  let availableNames = [...reviewNames];
  let availableTemplates = [...templates];

  for (let i = 0; i < numReviews; i++) {
    // Pick a name
    const nameIndex = Math.floor(rng() * availableNames.length);
    const name = availableNames.splice(nameIndex, 1)[0];

    // Pick a review template
    const templateIndex = Math.floor(rng() * availableTemplates.length);
    const comment = availableTemplates.splice(templateIndex, 1)[0];

    // Generate a random date within the last year
    const daysAgo = Math.floor(rng() * 365);
    const date = new Date();
    date.setDate(date.getDate() - daysAgo);

    // Generate a rating (mostly 5, sometimes 4)
    const rating = rng() > 0.2 ? 5 : 4;

    reviews.push({
      id: `${slug}-review-${i}`,
      user: name,
      date: date.toISOString().split('T')[0],
      rating: rating,
      comment: comment
    });
  }

  return reviews.sort((a, b) => new Date(b.date) - new Date(a.date));
};